package ejemplosJava;

public class Ejemplo_IfElseCarlo {

	public static void main(String[] args) {
		int numero = 30;

		if (numero > 20) {
			System.out.println("El numero de la variable es mayor a 20");

		} else {
			System.out.println("El numero de la variable es menor a 20");
		}
	}
}
